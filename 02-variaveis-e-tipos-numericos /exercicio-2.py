# 2. Faça um programa que peça para o usuário digitar três valores inteiro e imprima a soma deles.

print("Escreva tres numeros inteiros.")
numero = int(input("numero um: "))
numero2 = int(input("numero dois: "))
numero3 = int(input("numero tres: "))

soma = numero + numero2 + numero3

print(f"A soma dos numeros e {soma}")

